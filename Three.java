
//3> WAP in Java to print the table of any num from 1 to 10
public class Three {
    public static void main(String[] args)
    {
        int x = 5;
        System.out.println("Enter any number to print table");
        for(int i=1; i<11; i++)
        {
            System.out.println(x + "x"+ i + " = "+ x*i );
        }
    }
}
